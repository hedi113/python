def sumOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a + b
    return result
def substractOfTwoNumbers(a: float, b: float) -> float:
    subResult: float = None
    subResult = a - b
    return subResult
def divideTwoNumbers(a: float, b: float) -> float:
    divResult: float = None
    divResult = a / b
    return divResult
def multiplyTwoNumbers(a: float, b: float) -> float:
    multResult: float = None
    multResult = a * b
    return multResult
